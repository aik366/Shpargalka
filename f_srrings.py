#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
📚 ШПАРГАЛКА ПО F-СТРОКАМ (f-strings) В PYTHON
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
F-строки доступны с Python 3.6 (PEP 498) — самый быстрый и удобный способ
форматирования строк.
"""

# ──────────────────────────────────────────────────────────────────────────────
# 🔹 БАЗОВЫЙ СИНТАКСИС
# ──────────────────────────────────────────────────────────────────────────────

name = "Алиса"
age = 30
height = 1.68

print("🔹 БАЗОВЫЕ ПРИМЕРЫ")
print("=" * 70)
print(f"Привет, {name}!")                          # Привет, Алиса!
print(f"Возраст: {age}, рост: {height} м")         # Возраст: 30, рост: 1.68 м
print(f"Через 5 лет будет {age + 5} лет")          # Через 5 лет будет 35 лет
print(f"Имя заглавными: {name.upper()}")           # Имя заглавными: АЛИСА
print()

# ──────────────────────────────────────────────────────────────────────────────
# 🔹 ФОРМАТИРОВАНИЕ ЧИСЕЛ
# ──────────────────────────────────────────────────────────────────────────────

pi = 3.1415926535
price = 1999.99
big_num = 1234567

print("🔹 ФОРМАТИРОВАНИЕ ЧИСЕЛ")
print("=" * 70)
print(f"По умолчанию: {pi}")                       # 3.1415926535
print(f"2 знака после запятой: {pi:.2f}")          # 3.14
print(f"4 знака после запятой: {pi:.4f}")          # 3.1416
print(f"Ширина 10, 3 знака: {pi:10.3f}")           #      3.142
print(f"С разделителем: {big_num:,}")              # 1,234,567
print(f"С валютой: {price:,.2f} ₽")                # 1,999.99 ₽
print(f"Проценты: {0.85:.1%}")                     # 85.0%
print(f"Двоичная: {42:b}")                         # 101010
print(f"Шестнадцатеричная: {255:x}")               # ff
print(f"Шестнадцатеричная (загл.): {255:X}")       # FF
print(f"Экспонента: {12345:.2e}")                  # 1.23e+04
print()

# ──────────────────────────────────────────────────────────────────────────────
# 🔹 ВЫРАВНИВАНИЕ И ЗАПОЛНЕНИЕ
# ──────────────────────────────────────────────────────────────────────────────

text = "привет"

print("🔹 ВЫРАВНИВАНИЕ")
print("=" * 70)
print(f"По левому краю: '{text:<10}'")             # 'привет    '
print(f"По правому краю: '{text:>10}'")            # '    привет'
print(f"По центру: '{text:^10}'")                  # '  привет  '
print(f"Заполнитель '*': '{text:*^10}'")           # '**привет**'
print(f"Заполнитель '=': '{text:=^12}'")           # '===привет==='
print()

# ──────────────────────────────────────────────────────────────────────────────
# 🔹 ДАТЫ И ВРЕМЯ
# ──────────────────────────────────────────────────────────────────────────────

from datetime import datetime

now = datetime.now()

print("🔹 ФОРМАТИРОВАНИЕ ДАТЫ/ВРЕМЕНИ")
print("=" * 70)
print(f"Полная дата: {now:%Y-%m-%d}")              # 2026-02-08
print(f"Время: {now:%H:%M:%S}")                    # 14:30:45
print(f"Красиво: {now:%d.%m.%Y %H:%M}")            # 08.02.2026 14:30
print(f"День недели: {now:%A}, месяц: {now:%B}")   # Sunday, February
print()

# ──────────────────────────────────────────────────────────────────────────────
# 🔹 ОТЛАДКА (Python 3.8+)
# ──────────────────────────────────────────────────────────────────────────────

x = 42
y = 15

print("🔹 ОТЛАДКА С ПОМОЩЬЮ = (Python 3.8+)")
print("=" * 70)
print(f"{x=}")                                     # x=42
print(f"{y=}")                                     # y=15
print(f"{x+y=}")                                   # x+y=57
print(f"{x+y = }")                                 # x+y = 57 (с пробелами)
print(f"{x/y = :.2f}")                             # x/y = 2.80
print()

# ──────────────────────────────────────────────────────────────────────────────
# 🔹 УСЛОВИЯ И ВЫРАЖЕНИЯ ВНУТРИ {}
# ──────────────────────────────────────────────────────────────────────────────

score = 85
is_active = True

print("🔹 УСЛОВИЯ И ЛОГИКА")
print("=" * 70)
print(f"Статус: {'активен' if is_active else 'неактивен'}")  # активен
print(f"Оценка: {score} ({'отлично' if score >= 80 else 'удовл.'})")  # отлично
print(f"Квадрат числа: {(x := 7) ** 2}, x = {x}")  # 49, x = 7 (моржовый оператор)
print()

# ──────────────────────────────────────────────────────────────────────────────
# 🔹 МНОГОСТРОЧНЫЕ F-СТРОКИ
# ──────────────────────────────────────────────────────────────────────────────

user = "Боб"
balance = 1250.75

message = (
    f"Пользователь: {user}\n"
    f"Баланс: {balance:,.2f} ₽\n"
    f"Статус: {'✅ Активен' if balance > 0 else '❌ Заблокирован'}"
)

print("🔹 МНОГОСТРОЧНОЕ СООБЩЕНИЕ")
print("=" * 70)
print(message)
print()

# ──────────────────────────────────────────────────────────────────────────────
# 🔹 ЭКРАНИРОВАНИЕ СКОБОК
# ──────────────────────────────────────────────────────────────────────────────

print("🔹 ЭКРАНИРОВАНИЕ ФИГУРНЫХ СКОБОК")
print("=" * 70)
print(f"Вывод скобок: {{текст}}")                  # {текст}
print(f"JSON-подобный: {{'name': '{name}'}}")      # {'name': 'Алиса'}
print()

# ──────────────────────────────────────────────────────────────────────────────
# 🔹 ВЛОЖЕННОЕ ФОРМАТИРОВАНИЕ
# ──────────────────────────────────────────────────────────────────────────────

value = 42
width = 8
align = "^"

print("🔹 ВЛОЖЕННОЕ ФОРМАТИРОВАНИЕ")
print("=" * 70)
print(f"Динамическая ширина: {value:{width}d}")    #       42
print(f"Динамическое выравнивание: {value:{align}{width}d}")  #    42    
print()

# ──────────────────────────────────────────────────────────────────────────────
# 🔹 ТАБЛИЦА (ПРАКТИЧЕСКИЙ ПРИМЕР)
# ──────────────────────────────────────────────────────────────────────────────

print("🔹 ПРИМЕР: КРАСИВАЯ ТАБЛИЦА")
print("=" * 70)
print(f"{'N':>3} | {'Квадрат':>8} | {'Куб':>10}")
print("-" * 25)
for i in range(1, 6):
    print(f"{i:>3} | {i**2:>8} | {i**3:>10}")
print()

# ──────────────────────────────────────────────────────────────────────────────
# 🔹 СРАВНЕНИЕ ПРОИЗВОДИТЕЛЬНОСТИ (ИНФО)
# ──────────────────────────────────────────────────────────────────────────────

print("🔹 ПРОИЗВОДИТЕЛЬНОСТЬ")
print("=" * 70)
print("F-строки быстрее других методов форматирования:")
print("  • f-strings    — ⚡ самая высокая скорость")
print("  • str.format() — 🐢 на ~20-30% медленнее")
print("  • %-формат     — 🐌 на ~40-50% медленнее")
print()
print("✅ РЕКОМЕНДАЦИЯ: Используйте f-строки везде, где возможно!")
print()

# ──────────────────────────────────────────────────────────────────────────────
# ⚠️ ОГРАНИЧЕНИЯ И ОШИБКИ
# ──────────────────────────────────────────────────────────────────────────────

print("⚠️  ЧЕГО НЕЛЬЗЯ ДЕЛАТЬ В F-СТРОКАХ")
print("=" * 70)
print("❌ Комментарии внутри {}:  f'{x # коммент}' → SyntaxError")
print("❌ Обратные кавычки:      f'{`x`}' → SyntaxError")
print("❌ Динамический шаблон:   шаблон = 'Значение {x}'; f'{шаблон}' → не подставит x")
print()

# ──────────────────────────────────────────────────────────────────────────────
# 📚 КРАТКАЯ ПАМЯТКА ПО КОДАМ ФОРМАТИРОВАНИЯ
# ──────────────────────────────────────────────────────────────────────────────

print("📚 КОДЫ ФОРМАТИРОВАНИЯ")
print("=" * 70)
print("Целые числа:")
print("  :d  — десятичное целое")
print("  :b  — двоичное")
print("  :x  — шестнадцатеричное (строчные)")
print("  :X  — шестнадцатеричное (заглавные)")
print()
print("Дробные числа:")
print("  :f  — фиксированная точка")
print("  :.2f — 2 знака после запятой")
print("  :%  — проценты")
print("  :e  — экспоненциальная запись")
print()
print("Прочее:")
print("  :,  — разделитель тысяч")
print("  :<  — выравнивание влево")
print("  :>  — выравнивание вправо")
print("  :^  — центрирование")
print("  :=^10 — заполнитель '=' по центру, ширина 10")
print()
print("✨ Готово! Сохраните этот файл как fstrings_cheatsheet.py")